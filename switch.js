var x = 2;
switch (x) {
  case 0:
    text = "This is first case";
    console.log(text)
    break;
  case 1:
    text = "How are you ? 1";
    console.log(text)
    break;
  case 2:
    text = "I like web development";
    console.log(text)
    break;
  case 3:
    text = "I love react js";
    console.log(text)
    break;
  case 4:
    text = "app development";
    console.log(text)
    break;
  case 5:
    text = "learning javascript";
    console.log(text)
    break;
  case 6:
    text = "how to apply conditions";
    console.log(text)
    break;
  case 7:
    text = "studying switch functions";
    console.log(text)
    break;

  default:
    text = "No value found";
    console.log(text)
}
